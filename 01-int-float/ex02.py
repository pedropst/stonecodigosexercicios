"""
------------------------------------ PERGUNTA

Faça um programa que receba a base e altura de um triângulo e imprima a área dele.

"""

# ------------------------------------ RESPOSTA

base = float(input("Qual é a base do triângulo? "))
altura = float(input("Qual é a altura do triângulo? "))

area = (base*altura)/2

print(f"A área desse triângulo é: {area} unidades²")

